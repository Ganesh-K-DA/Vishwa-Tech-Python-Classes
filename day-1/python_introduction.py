# 4.what is IDE(Integrated Development Environment) tool
#  Ans> An IDE (Integrated Development Environment) is a software application that provides  
#        comprehensive facilities to computer programmers for software development.
   
# 5.what are the key components of IDE tool
#  Ans>   1. Code Editor: A text editor where developers write their code,
#         often with features like syntax highlighting, code completion, and error checking.
        
#          2. Compiler/Interpreter: Tools that convert the written code into a form that a computer can execute 
#           (compile for compiled languages or interpret for interpreted languages).
         
#          3. Debugger: A tool that helps identify and fix errors or bugs in the code by allowing step-by-step execution, 
#           breakpoints, and inspection of variables.
         
#          4. Build Automation Tools: Utilities that automate the process of compiling and running code, often 
#            integrated with testing frameworks.
         
#          5. Version Control Integration: Support for tools like Git that allow developers to track changes and collaborate with others.
           
